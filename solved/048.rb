#============================================================================
# euler048
# 6/22/12
#
# Find the last ten digits of 1^1 + 2^2 + ... + 1000^1000.
#============================================================================

def soln
	(1..1000).each.reduce { |a,i| a + i**i }.to_s.slice(-10, 10)
end

#=====================================
# Run
#=====================================

es = EulerSolution.new({:soln => soln})
es.run
